function color = muic()
    color = internal();
    for field_ = fieldnames(color)'
        field = field_{1};
        content = color.(field);
        if isstruct(content)
            for field2_ = fieldnames(content)'
                field2 = field2_{1};
                content2 = color.(field).(field2);
                color.(field).(field2) = hexstr_to_rgbarr(content2);
            end
        else
            color.(field) = hexstr_to_rgbarr(content);
        end
    end
end

